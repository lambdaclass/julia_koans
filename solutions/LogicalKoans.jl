module LogicalKoans

function not(x)
    !x
end

function equals(x, y)
    x == y
end

function not_equals(x, y)
    x != y
end

function less_than(x, y) 
    x < y
end

function greater_than(x, y) 
    x > y
end

function less_than_or_equal_to(x, y) 
    x <=y
end

function greater_than_or_equal_to(x, y) 
    x >= y
end

end